using UnityEngine;

/// <summary>
/// Вид/тело как данные — один ассет на вид («Человек», «Волк»). Две роли:
///  • ШАССИ — базовое тело: ёмкость пула + органы по умолчанию для каждого слота;
///  • ДОНОР — источник органов, которые ставятся в слоты шасси при химеризации.
/// Органы лежат внутри вида (не отдельными файлами). Готовит смену шасси и мультивид.
/// </summary>
[CreateAssetMenu(menuName = "Chimera/Вид (SpeciesSO)", fileName = "Species")]
public class SpeciesSO : ScriptableObject
{
    public string speciesName = "Вид";
    public Color tint = Color.gray;   // цвет тела при озверении этим видом (лерп шкалы мозга)
    public int mutagenPool = 10;      // ёмкость пула, когда вид = шасси
    public Organ[] organs;            // органы вида (по одному на покрываемый слот)
}

/// <summary>
/// Орган: вклад в статы своего слота (абсолютные значения; человеческие = базовая норма).
/// Каждый орган заполняет только «свои» статы, остальные = 0 — суммирование по слотам даёт итог.
/// </summary>
[System.Serializable]
public class Organ
{
    public string organName = "Орган";
    public string slot = "Руки";   // в какой слот шасси ставится
    public string hotkey = "1";    // временный бинд клавиши (MVP-конструктор)
    public int cost;               // цена в пуле

    public int damage, maxHp, lifeSteal;
    public int venomStacks;        // яд на укусе (змеиные клыки): стаков за попадание
    public int bleedStacks;        // кровотечение на укусе (волчьи клыки): стаков за попадание
    public float range, atkCooldown, moveSpeed, dashSpeed, dashCooldown, damageReduction, regen, regenOOC;
    public float dashDuration;     // своя длина рывка ног (0 = дефолт контроллера): лосиные — длинный таран, волчьи — короткий
    public float thermalRange;     // радиус термозрения (Пит-орган); фикс-фича, как range — не блендится
    public float howlStunAt;       // ПОРОГ-ФИЧА (3-я ось экспрессии): мощь носителя ≥ этого — вой ещё и СТАНИТ.
                                   // 0 = стана нет вовсе. Не «сколько», а ЧТО ВООБЩЕ открывается: рядовой волк
                                   // (Э 0.45) только зовёт стаю, вервольф (Э 2) и игрок на 100 родства — глушат
    public float howlRadius;       // ГОЛОС Пасти: база радиуса воя (0 = не воет). Итог = база × мощь-превосходство
                                   // (тело считает: игрок BonusMult ×1..2, NPC max(1, Э) — норму вниз не штрафуем)
    public bool enablesBite, enablesScent, enablesKick, enablesHowl, coldBlooded, camo, enablesThermal, enablesConstrict;
    public bool insight;           // ЧУТЬЁ УЧЁНОГО (человеческое Чутьё): наблюдательность вместо звериного нюха —
                                   // РАСПОЗНАЁТ намерение по замаху (цвет приёма вместо «что-то готовит») и читает
                                   // состояния ЧИСЛОМ. Аналитика — сила человека: единственная фича этого слота,
                                   // за которую платят человечностью наоборот — сохранением её
    public bool enablesBellow;     // РЁВ (Глотка лося): детонация ярости на месте — кин-лоси в берсерк, чужих пугает
    public bool enablesAntler;     // РОГА (придаток лося, химерный слот): фронтальный удар — откидывание + кровотечение
    public bool enablesCharge;     // ТАРАН (Лосиные ноги): рывок становится горящим — урон+откидывание по пути («основа боя — копыта»)
    public bool chassisOnly;       // ходовая часть/локомоция шасси: аугументом НЕ крадётся (закон: локомоция = свойство шасси)
    public bool digestion;         // переваривание (тело змеи): убил добычу → СЫТ, бонус-реген до полного HP (компонент Digestion)
    public string nativeChassis;   // РОДНОЕ ШАССИ приёма (2-я ось экспрессии): полная форма открыта только на этом шасси, на чужом — кап. Пусто = нет гейта. Носитель — Хвост (ст.3 удушения только на змеином шасси)
}
