using UnityEngine;

/// <summary>
/// ЕДИНАЯ цветовая легенда сигналов — общий язык для всех существ, ТРИ раздела:
///  • ПРИЁМЫ (телеграф-вспышка): цвет на существе = действие, которое ОНО САМО замахивается совершить;
///  • СТАТУСЫ: состояние существа, единое от ЛЮБОГО источника (стан от воя = удержание обхватом);
///  • ЭМОЦИИ (рест-тинт, идея пользователя): моральное состояние ПОДКРАШИВАЕТ тело, пока действует —
///    слабее вспышек приёмов (приоритет-стек Telegraph: стан > приём > эмоция > натуральный). Плейсхолдер
///    под видоспец боди-ленгвидж (хвост/уши/оскал — с ригами).
/// Реляционных цветов («схвачен ИМЕННО игроком») не заводим — кто виновник, ясно из контекста.
/// Один источник правды: цвет означает одно и то же у волка, вервольфа и будущих видов.
/// (Пока статик; если понадобится крутить в редакторе — повысим до ScriptableObject.)
/// </summary>
public static class TelegraphColors
{
    // приёмы
    public static readonly Color Bite   = new(1f, 0.28f, 0.20f);    // укус — красный
    public static readonly Color Leap   = new(1f, 0.60f, 0.10f);    // прыжок — оранжевый
    public static readonly Color Grab   = new(0.70f, 0.20f, 0.90f); // захват — фиолетовый
    public static readonly Color Charge = new(0.90f, 0.10f, 0.50f); // чардж — розовый
    public static readonly Color Howl   = new(0.60f, 0.50f, 1f);    // вой — бледно-синий
    public static readonly Color Antler = new(0.20f, 0.85f, 0.60f); // рога — зелёно-бирюзовый (протыкание, не таран)
    public static readonly Color Sword  = new(0.55f, 0.75f, 0.95f); // меч (человек) — стальной
    public static readonly Color Kick   = new(0.80f, 0.85f, 0.25f); // пинок (человек) — лаймовый

    // НЕРАСПОЗНАННОЕ НАМЕРЕНИЕ: «что-то готовит», но что — не разобрать. Так замах выглядит БЕЗ человеческого
    // Чутья: факт виден всем (окно реакции то же), смысл открывает наблюдательность.
    // На телах это НЕ отдельный цвет, а ОСВЕТЛЕНИЕ собственного (`Telegraph.UnknownLift`) — зверь остаётся
    // собой, просто «зажигается». Здесь — только образец для легенды F1
    public static readonly Color Unknown = new(0.82f, 0.80f, 0.76f);

    // статусы
    public static readonly Color Stunned = new(0.92f, 0.92f, 0.85f); // «выключен из боя» (стан/схвачен) — белёсый
    public static readonly Color Venom   = new(0.45f, 0.85f, 0.20f); // яд — ядовито-зелёный
    public static readonly Color Bleed   = new(0.72f, 0.04f, 0.12f); // кровотечение — тёмно-алый
    // (Bleed соседствует с RageTint по тону, но живёт в ДРУГОМ канале: кровь — значок в HUD, ярость — тинт морды.)

    // эмоции (рест-тинт тела; ярость ≠ красный укуса — глубокий бордовый, страх — явный холодный синий)
    public static readonly Color RageTint = new(0.72f, 0.10f, 0.10f); // ярость: тело наливается бордовым
    public static readonly Color FearTint = new(0.25f, 0.42f, 0.90f); // страх/паника: тело синеет (на сером волке читаемо)
}
