using UnityEngine;

/// <summary>
/// ЯДОУПОРНОСТЬ — маркер-компонент по образцу `ColdBlooded`: висит на теле, опрашивается ядом.
/// Живёт на Сердце ежа (и придёт игроку тем же органом), делая его естественным контр-видом змеи.
///
/// НЕ ИММУНИТЕТ — и это принципиально. Биология даёт ежу резист в десятки раз, но не полную
/// неуязвимость; полный иммунитет к тому же выключил бы змею как противника вместо того, чтобы дать
/// ежу преимущество. Поэтому режем не сам стак, а его ЖИЗНЬ: токсин разлагается почти сразу.
///
/// Следствие: одиночный укус по-прежнему «проходит», но яд НЕ НАКАПЛИВАЕТСЯ — до 2-го стака
/// (уязвимость) и 3-го (DoT) змее пришлось бы попасть несколько раз за доли секунды.
/// </summary>
public class VenomResist : MonoBehaviour
{
    [SerializeField, Range(0.02f, 1f)] float durationMult = 0.15f; // во сколько раз короче живёт стак яда

    public float DurationMult => Mathf.Clamp(durationMult, 0.02f, 1f);
}
