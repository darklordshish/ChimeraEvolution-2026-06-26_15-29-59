using UnityEngine;

/// <summary>
/// КРОВЕУПОРНОСТЬ — маркер-компонент по образцу `VenomResist`: висит на теле, опрашивается кровотечением.
/// Живёт на Лосином сердце (и придёт игроку тем же органом): у ежа сердце против яда, у лося — против крови,
/// чтобы у массивного травоядного было СВОЁ преимущество, а не только гора HP.
///
/// НЕ ИММУНИТЕТ (как и ядоупорность): режем не сам порез, а ЖИЗНЬ стака — рана быстро затягивается на
/// толстой шкуре. Одиночный укус кровит, но НЕ НАКАПЛИВАЕТСЯ до порога кровопотери; сбить лося кровью
/// сможет только СТАЯ, наваливая порезы быстрее, чем они заживают. Кровь особенно опасна массивным
/// (урон в % от МАКС HP), так что резист лосю ценен вдвойне.
/// </summary>
public class BleedResist : MonoBehaviour
{
    [SerializeField, Range(0.05f, 1f)] float durationMult = 0.3f; // во сколько раз короче живёт стак крови

    public float DurationMult => Mathf.Clamp(durationMult, 0.05f, 1f);
}
