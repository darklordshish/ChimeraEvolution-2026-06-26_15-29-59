/// <summary>
/// Тот, кто держит игрока в захвате. Игрок дёргает BreakFree, когда пытается сорваться рывком.
/// Возвращает true, если захват ОТПУСТИЛ (тогда игрок свободен и получает урон срыва). Волк рвётся
/// всегда; змея на обхвате — только в 1-й стадии (позже сжатие держит, рывок бесполезен — false).
/// Развязывает PlayerController; реализуют WolfPsyche и SnakePsyche.
/// </summary>
public interface IGrabber
{
    bool BreakFree(int damage);
}
