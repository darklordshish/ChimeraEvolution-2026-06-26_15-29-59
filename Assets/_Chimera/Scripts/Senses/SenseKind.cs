/// <summary>
/// Оси чувств (сенсорный слайс S1). Новое чувство (совиный слух, ежиный тонкий нюх) = новое значение здесь
/// + канал в профиле (`Senses`) с безопасным дефолтом (range 0 = чувства у вида нет). Существующие виды правок не требуют.
/// </summary>
public enum SenseKind { Sight, Thermal, Scent, Hearing }

/// <summary>
/// Канал ОДНОГО чувства в профиле вида: базовая дальность + острота (порог чувствительности, задел под S2) +
/// множители дальности ПО СОСТОЯНИЮ восприятия (Спокойствие/Настороженность/Атака). Это и есть матрица
/// «чувство × состояние» — форма выразительная, наполнение ленивое (у волка/змеи множители =1 до среза 4).
/// </summary>
// НАПРАВЛЕННОСТЬ чувства (`viewHalfAngle`): ЗРЕНИЕ — КОНУС (полу-угол; хищник узкий ~60°, травоядное широкое до
// кругового) → «прошмыгнуть» работает ПО НАПРАВЛЕНИЮ (со спины/сбоку не видит) + порог «в упор» (вплотную чуют вне
// конуса). Тот же атрибут = зерно first-person-FOV камеры. ТЕРМО/ЗАПАХ — круговые (180): тепло за спину не спрятать,
// след нюхают. Чек зрения — ОДИН на всех: `Perception.Sees` (дальность+конус/«в упор»+камуфляж+стена);
// психика лишь зовёт его со своим радиусом «в упор». Запах-облако+ветер — средовой слайс.
[System.Serializable]
public class SenseChannel
{
    public float range = 0f;         // базовая дальность; 0 = чувства у вида нет
    public float acuity = 1f;        // порог чувствительности (S2 задействует эмиттер-силу; пока задел)
    public float viewHalfAngle = 180f; // сектор обзора: полу-угол (180 = круговое). Зрение — узкий конус; термо/запах — 180
    public float calmMult = 0.4f;  // СПОКОЕН — чувства расслаблены, заметно слепее: подкрасться/прошмыгнуть мимо (срез 4: рычаг)
    public float waryMult = 1f;    // НАСТОРОЖЕН — базовая дальность (НЕ раздуваем: иначе алертная стая находит издалека; «обострение» позже = шире КОНУС, не дальше)
    public float attackMult = 1f;  // АТАКА — фокус на цели: базовая дальность

    public float For(Alert s) => range * (s == Alert.Attack ? attackMult : s == Alert.Wary ? waryMult : calmMult);
}
